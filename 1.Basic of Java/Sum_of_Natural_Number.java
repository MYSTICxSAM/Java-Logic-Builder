public class Sum_of_Natural_Number {
    public static void main(String[] args) 
    {
        int i, num = 10, sum = 0;
        
        for (i = 1; i <= num; ++i) 
        {
            
            sum = sum + i;
        }
        // prints the sum
        System.out.println("Sum of First 10 Natural Numbers is = " + sum);
    }
}
